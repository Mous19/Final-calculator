package org.example;

public class Operations {
    public static int myAdd (int first, int second) {
        return first + second;
    }
    public static int mySub (int first, int second) {
        return first - second;
    }
    public static int myMultiple (int first, int second) {
        return first * second;
    }
    public static int myDivided (int first, int second) {
        return first / second;
    }
}

